#include<bits/stdc++.h>
using namespace std;

int main() {

    // for loop syntax
    for(int i = 0; i < 5; i++) {
        cout << "Ravi Somani For Loop Count: " << i + 1 << endl;
    }

    // while loop syntax
    int i = 10;
    while(i < 15) {
        cout << "Ravi Somani While Loop Count: " << i + 1 << endl;
        i++;
    }

    // do while loop syntax
    int count = 50;
    do{
        cout << "Ravi Somani Do While Loop Count: " << count + 1 << endl;
        count++;
    } while (count < 55);
    

    return 0;
}